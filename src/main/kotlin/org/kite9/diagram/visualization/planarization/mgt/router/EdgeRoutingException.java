package org.kite9.diagram.visualization.planarization.mgt.router;


public class EdgeRoutingException extends RuntimeException {

	private static final long serialVersionUID = -6142985734760809914L;

	public EdgeRoutingException() {
		super();
	}

	public EdgeRoutingException(String message, Throwable cause) {
		super(message, cause);
	}

	public EdgeRoutingException(String message) {
		super(message);
	}

	public EdgeRoutingException(Throwable cause) {
		super(cause);
	}

}
